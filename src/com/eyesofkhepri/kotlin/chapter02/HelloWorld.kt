package com.eyesofkhepri.kotlin.chapter02

// 함수 선언시 fun을 사용한다.
// 파라메터 뒤에 파라메터의 타입을 쓴다, 변수도 마찬가지다.
// 함수를 꼭 클래스에 넣을 필요가 없다.
fun main(args: Array<String>) {
    // System.out.println말고 간결히 쓸수 있는 레퍼들을 제공한다. println은 그중 하나이다.
    // 좀더 간편하게 쓸수 잇다.
    println("Hello World.");    // 줄 끝에 세미콜론(';')을 넣지 않아도 된다.
}
